#include "libft.h"

void	ft_free(char **tab)
{
	int i;

	i = 0;
	while (tab[i])
		i++;
	while (i >= 0)
	{
		if (tab[i] != NULL)
		{
			free(tab[i]);
			tab[i] = NULL;
		}
		i--;
	}
	free(tab);
}
